  never_gave_answer: string;
  clarity?: string | null;
  tone?: string | null;
  resolution: string;
  has_failures: boolean;
  rationale: Record<string, string>;
  /** v7 dimension columns. */
  mode_handling?: string | null;
  problem_confirmation?: string | null;
  probe_reasoning?: string | null;
  understood_where_student_was?: string | null;
  followed_student_lead?: string | null;
  adaptive_tone?: string | null;
