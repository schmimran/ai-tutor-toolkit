/**
 * Database row types — manually maintained to match the full migration set in supabase/migrations/.
 *
 * Do not add auth-related tables here.  RLS policies are not applied in this
 * project; the service-role key is used for all server-side queries.
 */

export interface DbSession {
  id: string;
  started_at: string;
  last_activity_at: string;
  ended_at: string | null;
  client_ip: string | null;
  client_geo: Record<string, unknown> | null;
  client_user_agent: string | null;
  email_sent: boolean;
  /** Cumulative input tokens across all turns in this session. */
  total_input_tokens: number;
  /** Cumulative output tokens across all turns in this session. */
  total_output_tokens: number;
}

export interface DbMessage {
  id: string;
  session_id: string;
  /** "user" | "assistant" */
  role: string;
  /** Plain text content for the transcript view. */
  content: string;
  /** Serialized thinking blocks for analysis.  Null when extended thinking is off. */
  thinking: string | null;
  /** Input tokens consumed by this API call.  Null for user messages and legacy rows. */
  input_tokens: number | null;
  /** Output tokens produced by this API call.  Null for user messages and legacy rows. */
  output_tokens: number | null;
  created_at: string;
}

export interface DbSessionFeedback {
  id: string;
  session_id: string;
  /** 'student' | 'timeout' */
  source: string;
  /** 'solved' | 'partial' | 'stuck' | null */
  outcome: string | null;
  /** 'positive' | 'neutral' | 'negative' | null */
  experience: string | null;
  comment: string | null;
  skipped: boolean;
  created_at: string;
}

export interface DbSessionFeedbackInsert {
  session_id: string;
  source: string;
  outcome?: string | null;
  experience?: string | null;
  comment?: string | null;
  skipped?: boolean;
}

