/**
 * Database row types — manually maintained to match supabase/migrations/001_initial_schema.sql.
 *
 * Do not add auth-related tables here.  RLS policies are not applied in this
 * project; the service-role key is used for all server-side queries.
 */

export interface DbSession {
  id: string;
  started_at: string;
  last_activity_at: string;
  ended_at: string | null;
  client_ip: string | null;
  client_geo: Record<string, unknown> | null;
  client_user_agent: string | null;
  email_sent: boolean;
  /** Cumulative input tokens across all turns in this session. */
  total_input_tokens: number;
  /** Cumulative output tokens across all turns in this session. */
  total_output_tokens: number;
}

export interface DbMessage {
  id: string;
  session_id: string;
  /** "user" | "assistant" */
  role: string;
  /** Plain text content for the transcript view. */
  content: string;
  /** Serialized thinking blocks for analysis.  Null when extended thinking is off. */
  thinking: string | null;
  /** Input tokens consumed by this API call.  Null for user messages and legacy rows. */
  input_tokens: number | null;
  /** Output tokens produced by this API call.  Null for user messages and legacy rows. */
  output_tokens: number | null;
  created_at: string;
}

export interface DbFeedback {
  id: string;
  session_id: string;
  /** FK → messages(id).  The assistant message this feedback rates.  Null for legacy rows. */
  message_id: string | null;
  /** "accuracy" | "usefulness" | "tone".  Null for legacy rows. */
  category: string | null;
  /** 1–5 star rating.  Null if the category was not rated (N/A). */
  rating: number | null;
  comment: string | null;
  created_at: string;
}

/** Insert payloads — id and created_at are generated by the database. */
export type DbSessionInsert =
  Omit<DbSession, "id" | "started_at" | "last_activity_at" | "ended_at" | "total_input_tokens" | "total_output_tokens"> &
  Partial<Pick<DbSession, "id" | "started_at" | "last_activity_at" | "total_input_tokens" | "total_output_tokens">>;

export type DbMessageInsert = Omit<DbMessage, "id" | "created_at">;

/** Explicit insert type so message_id/category are optional — legacy callers still work. */
export interface DbFeedbackInsert {
  session_id: string;
  message_id?: string | null;
  category?: string | null;
  rating?: number | null;
  comment?: string | null;
}

/** Update payloads — all fields optional except the primary key. */
export type DbSessionUpdate = Partial<Omit<DbSession, "id">>;

export interface DbDisclaimerAcceptance {
  id: string;
  accepted_at: string;
  client_ip: string | null;
  client_geo: Record<string, unknown> | null;
  client_user_agent: string | null;
  /** FK → sessions(id).  Null until backfilled after first chat message. */
  session_id: string | null;
  /** The client-generated session UUID stored at acceptance time (no FK).
   *  Used to backfill session_id after the sessions row is created. */
  client_session_id: string | null;
}

export interface DbDisclaimerAcceptanceInsert {
  client_ip?: string | null;
  client_geo?: Record<string, unknown> | null;
  client_user_agent?: string | null;
  session_id?: string | null;
  client_session_id?: string | null;
}
